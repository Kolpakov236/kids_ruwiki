"""
LLM-based semantic content moderation for children's encyclopedia.
Conservative stance: when borderline — block.
"""
from __future__ import annotations

import json
import logging
import re

logger = logging.getLogger(__name__)

_SYSTEM = (
    "Ты — строгий модератор детской энциклопедии для детей 6–14 лет. "
    "Твоя задача — не пропускать НИЧЕГО неподходящего. "
    "При малейшем сомнении — блокируй.\n"
    'Отвечай ТОЛЬКО валидным JSON: {"safe": true} или {"safe": false, "reason": "одна строка"}'
)

_RULES = """\
═══ ОБЯЗАТЕЛЬНО БЛОКИРУЙ ═══

СЕКСУАЛЬНЫЙ КОНТЕНТ — блокируй ЛЮБОЕ упоминание:
• Половые органы (любые названия, включая детские и медицинские: пенис, вагина, писюн, попка в сексуальном смысле)
• Секс, сексуальные действия, эротика, порнография — явные и завуалированные («как занимаются любовью», «что делают взрослые ночью»)
• Размножение ЛЮДЕЙ (не животных!) если вопрос не строго анатомический и школьный
• Менструация, сперма, эрекция, оргазм

НАСИЛИЕ И ОПАСНОСТЬ:
• Как убить / причинить вред конкретному человеку или себе
• Как сделать оружие, яд, взрывчатку
• Суицид, самоповреждение — любые формы
• Пытки, издевательства, буллинг с инструкцией «как делать»

НАРКОТИКИ И ПАВ:
• Любые вопросы о приёме, покупке, синтезе наркотиков и психоактивных веществ
• Алкоголь — если вопрос «как напиться», «сколько выпить»
• Курение — если вопрос «как начать курить»

ПОЛИТИКА — блокируй:
• Пропаганда конкретных политических партий, лидеров, идеологий
• «Кто лучший президент/политик», «за кого голосовать»
• Сравнение политических систем с явной предвзятостью
• Вопросы о выборах, политических скандалах, коррупции (это не детская тема)

РЕЛИГИЯ И УБЕЖДЕНИЯ — блокируй:
• Критика или высмеивание любой религии
• «Какая религия правильная/лучшая»
• Религиозный экстремизм, радикальные течения

РАСИЗМ И ДИСКРИМИНАЦИЯ — блокируй:
• Вопросы типа «почему [раса/народ] хуже/лучше»
• Оскорбления по национальности, расе, внешности
• Стереотипы о народах с негативным подтекстом

НЕЦЕНЗУРНАЯ ЛЕКСИКА:
• Маты, оскорбления, унизительные прозвища

═══ РАЗРЕШАЙ (НЕ блокируй) ═══
• Школьные предметы: физика, химия, биология, математика, история, география
• Размножение ЖИВОТНЫХ и РАСТЕНИЙ — это биология
• Строение тела человека — если это анатомия в учебном контексте (органы пищеварения, кровообращение и т.д.)
• История войн, революций, катастроф — как исторические факты
• Религии мира — как культурный и исторический феномен (что такое христианство, ислам и т.д.)
• Страны, народы, культуры — нейтрально и образовательно
• Технологии, космос, природа, животные, изобретения
• Спорт, искусство, музыка, кино

═══ ПРИНЦИП ═══
Если запрос касается любой из заблокированных тем — даже косвенно или в «невинной» формулировке — отвечай {"safe": false}.
Детская энциклопедия — это место для знаний, а не для взрослых тем.\
"""


async def validate_query_with_llm(query: str) -> tuple[bool, str]:
    """
    Semantic content moderation via LLM.
    Returns (is_blocked: bool, reason: str).
    Fails open on errors — for availability.
    """
    from app.services.llm import _call_provider, _content_from_response

    user_msg = f"{_RULES}\n\nЗапрос для проверки: «{query[:300]}»\n\nJSON:"
    messages = [
        {"role": "system", "content": _SYSTEM},
        {"role": "user", "content": user_msg},
    ]

    try:
        raw = await _call_provider(messages)
        content = _content_from_response(raw).strip()

        m = re.search(r"\{[^{}]*\}", content, re.DOTALL)
        if m:
            data = json.loads(m.group(0))
            safe = bool(data.get("safe", True))
            reason = str(data.get("reason", ""))
            if not safe:
                logger.info("llm_moderation BLOCKED: query=%r reason=%r", query[:80], reason)
            return (not safe, reason)

        # Free-text fallback
        low = content.lower()
        if any(kw in low for kw in ('"safe": false', "false", "небезопасн", "заблокировать", "нельзя")):
            return (True, content[:120])

        return (False, "")

    except Exception as exc:
        logger.warning("llm_moderation failed (fail-open): %s", exc)
        return (False, "")
